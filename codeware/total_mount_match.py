
# حتاج إلى كتابة دالة في لغة البرمجة تقوم بحساب عدد النقاط التي حصل عليها فريق الكرة الخاص بك في البطولة. النقاط تُحسب وفقًا للشروط التالية:

# إذا كانت نتيجة المباراة "x:y" حيث x > y، يحصل الفريق على 3 نقاط (الفوز).
# إذا كانت نتيجة المباراة "x:y" حيث x < y، لا يحصل الفريق على نقاط (الخسارة).
# إذا كانت نتيجة المباراة "x:y" حيث x = y، يحصل الفريق على نقطة واحدة (التعادل).
# وفقًا للملاحظات، يلعب الفريق دائمًا 10 مباريات في البطولة، وتتراوح قيم x و y بين 0 و 4.


def points(games):
    points_number = 0

    for match in games:
        x, y = map(int, match.split(':')) 
        if x > y:
            points_number += 3
        elif x == y:
            points_number += 1
        else:
            # لا تقوم بطرح 3 نقاط هنا، بل اترك الخيار للتفاوض
            pass

    return points_number

print(points(['1:2', '1:4', '2:1']))

