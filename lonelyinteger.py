# المسألة بتقول إن فيه مصفوفة من الأرقام، وكل الأرقام في المصفوفة مكررة ماعدا رقم واحد. المطلوب إنك تلاقي الرقم الوحيد اللي مش مكرر.

# مثال:
# لو كانت المصفوفة زي كدا: [2, 4, 5, 2, 4]
# تشوف إن كل الأرقام مكررة ماعدا الـ 5، فالرقم الوحيد اللي مش مكرر هو 5.



def lonelyinteger(nums):
    for i in nums:
        if nums.count(i)==1:
            return i


print(lonelyinteger([2,4,2,3,3,4,1]))